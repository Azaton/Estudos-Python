# Função para reproduzir sequência de números inteiros a partir de um início (inclusivo) para um fim (exclusivo)
# Usarmos range (i,j)
# Recebe 3 argumentos: stop (obrigatório), start e step

for numero in range(0,11): #range com for
    print(numero, end=" ")

for numero in range(0, 51, 7): #range com for
    print(numero, end=" ")