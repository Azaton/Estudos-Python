# AULA "Tipos de Operadores com Python"

saldo = 500
saldo /= 5
print(saldo)

saldo = 500  # Você declara uma variável e o seu valor
saldo %= 333 # Operação de módulo entre o valor atual de saldo (500) e 333. O resultado dessa operação é atribuído novamente à variável saldo.
print(saldo)
saldo //= 2  # Valor é atribuído 
saldo += 9
print(saldo)
saldo += 50
print(saldo)
saldo **= 2
print(saldo)